package cors

import (
	"bytes"
	"reflect"
	"strconv"
	"strings"
	"time"
	"unsafe"
)

type converter func(string) string

func generateNormalHeaders(c Config) map[string]string {
	headers := make(map[string]string)
	if c.AllowCredentials {
		headers["Access-Control-Allow-Credentials"] = "true"
	}
	if len(c.ExposeHeaders) > 0 {
		exposeHeaders := convert(normalize(c.ExposeHeaders), normalizeHeaderKey)
		headers["Access-Control-Expose-Headers"] = strings.Join(exposeHeaders, ",")
	}
	if c.AllowAllOrigins {
		headers["Access-Control-Allow-Origin"] = "*"
	} else {
		headers["Vary"] = "Origin"
	}
	return headers
}

func generatePreflightHeaders(c Config) map[string]string {
	headers := make(map[string]string)
	if c.AllowCredentials {
		headers["Access-Control-Allow-Credentials"] = "true"
	}
	if len(c.AllowMethods) > 0 {
		allowMethods := convert(normalize(c.AllowMethods), strings.ToUpper)
		value := strings.Join(allowMethods, ",")
		headers["Access-Control-Allow-Methods"] = value
	}
	if len(c.AllowHeaders) > 0 {
		allowHeaders := convert(normalize(c.AllowHeaders), normalizeHeaderKey)
		value := strings.Join(allowHeaders, ",")
		headers["Access-Control-Allow-Headers"] = value
	}
	if c.MaxAge > time.Duration(0) {
		value := strconv.FormatInt(int64(c.MaxAge/time.Second), 10)
		headers["Access-Control-Max-Age"] = value
	}
	if c.AllowAllOrigins {
		headers["Access-Control-Allow-Origin"] = "*"
	} else {
		// Always set Vary headers
		// see https://github.com/rs/cors/issues/10,
		// https://github.com/rs/cors/commit/dbdca4d95feaa7511a46e6f1efb3b3aa505bc43f#commitcomment-12352001

		headers["Vary"] = "Origin"
	}
	return headers
}

func normalize(values []string) []string {
	if values == nil {
		return nil
	}
	distinctMap := make(map[string]bool, len(values))
	normalized := make([]string, 0, len(values))
	for _, value := range values {
		value = strings.TrimSpace(value)
		value = strings.ToLower(value)
		if _, seen := distinctMap[value]; !seen {
			normalized = append(normalized, value)
			distinctMap[value] = true
		}
	}
	return normalized
}

func convert(s []string, c converter) []string {
	var out []string
	for _, i := range s {
		out = append(out, c(i))
	}
	return out
}

func str2bytes(s string) (b []byte) {
	/* #nosec G103 */
	bh := (*reflect.SliceHeader)(unsafe.Pointer(&b))
	/* #nosec G103 */
	sh := (*reflect.StringHeader)(unsafe.Pointer(&s))
	bh.Data = sh.Data
	bh.Len = sh.Len
	bh.Cap = sh.Len
	return b
}

func bytes2str(b []byte) string {
	return *(*string)(unsafe.Pointer(&b))
}

func compareByteSlices(origin []byte, patterns ...[]byte) int {
	cmp := make([]byte, 0, len(origin))
	for _, v := range patterns {
		cmp = append(cmp, v...)
	}
	return bytes.Compare(origin, cmp)
}

func normalizeHeaderKey(s string) string {
	b := []byte(s)

	n := len(b)
	if n == 0 {
		return ""
	}

	b[0] = toUpperTable[b[0]]
	for i := 1; i < n; i++ {
		p := &b[i]
		if *p == '-' {
			i++
			if i < n {
				b[i] = toUpperTable[b[i]]
			}
			continue
		}
		*p = toLowerTable[*p]
	}
	return bytes2str(b)
}

